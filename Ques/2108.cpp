// 2108. Find First Palindromic String in the Array
#include <iostream>
#include <bits/stdc++.h>
using namespace std;

int main()
{
    vector<string> str;
    str.push_back("HEllo");
    str.push_back("iQlipse");
    for (int i = 0; i < str[0].length(); i++)
    {
        cout << str[0][i]<<endl;
    }
    return 0;
}